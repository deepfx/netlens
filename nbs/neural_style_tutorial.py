def rename_layer(i, layer, prefix):
    prefix = module_prefix_map.get(layer.__class__)
    if prefix is None:
        raise RuntimeError('Unrecognized layer: {}'.format(layer.__class__.__name__))
    return f'{prefix}_{i}', layer


